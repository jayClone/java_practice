public class casting {
    //casting method
    public static void main(String[] args){
        //implicit casting
        int i=10;
        double d=i;
        System.out.println("i="+i+" d="+d);
        //explicit casting
        double d2=10.5;
        int i2=(int)d2;
        System.out.println("d2="+d2+" i2="+i2);
    }
}
